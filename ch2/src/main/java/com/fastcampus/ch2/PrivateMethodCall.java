package com.fastcampus.ch2;

import java.lang.reflect.Method;

public class PrivateMethodCall {
	public static void main(String[] args) throws Exception{
//		Hello hello = new Hello();
//		hello.main(); // private이라서 외부 호출 불가
		//------------------------------------	
		// Hello 에서 url 호출이 가능한 이유
		// Reflection API를 사용 - 클래스 정보를 얻고 다룰 수 있는 강력한 기능 제공
		// java.lang.reflect 패키지를 제공
		//-----------↓↓↓↓↓↓↓↓↓↓↓↓-------------
		// 다른 클래스에서 private 클래스를 호출한다.
		// Hello 클래스의 Class객체(클래스의 정보를 담고있는 객체)를 얻어온다
		Class helloClass = Class.forName("com.fastcampus.ch2.Hello");
		Hello hello = (Hello)helloClass.newInstance(); // Class 객체가 가진 정보로 객체 생성
					  // newInstance()의 반환값이 object라서 형변환해줘야한다.
		Method main = helloClass.getDeclaredMethod("main");
		main.setAccessible(true); // private인 main()을 호출가능하게한다.
		
		main.invoke(hello); // hello.main() 호출한것과 같다.
		
		
	}
}
